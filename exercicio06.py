"""
6. Desenvolva um programa que leia um número inteiro e apresente o número
informado, seguido do seu antecessor e do seu sucessor. Use variaveis para
desenvolver o programa.
"""

n1 = int(input("Digite um numero inteiro:"))

antecessor = n1 - 1
sucessor = n1 + 1

print("Numero digitado:", n1)
print("Atecessor:", antecessor)
print("Sucessor:", sucessor)
