L = [1, 2, 3, 4, 5]

print(L[0:5]) # [1, 2, 3, 4, 5] da posição 0 até a posição 5, sem inclui-la
print(L[:5])  # [1, 2, 3, 4, 5] do inicio até a posição 5, sem inclui-la
print(L[:-1]) # [1, 2, 3, 4] do inicio até o ultimo, sem inclui-lo
print(L[1:3]) # [2, 3] da posição 1 até a posição 3, sem inclui-la
print(L[1:4]) # [2, 3, 4] da posição 1 até a posição 4, sem inclui-la
print(L[3:])  # [4, 5] da posição 3 até o final
print(L[:3])  # [1, 2, 3] do inicio até a posição 3, sem inclui-la
print(L[-1])  # 5 do ultimo elemento
print(L[-2])  # 4 do penultimo elemento
